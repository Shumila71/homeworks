def main(x):
    lookup_table = {
        (2006, 1970, x[2], 1966): 2,
        (2006, 1970, x[2], 1959): 1,
        (2006, 1970, x[2], 1988): 0,
        (2006, 1979, x[2], x[3]): 3,
        (2006, 1969, x[2], x[3]): 4,
        (2007, x[1], 2013, x[3]): 5,
        (2007, x[1], 1968, 1988): 6,
        (2007, x[1], 1968, 1959): 7,
        (2007, x[1], 1968, 1966): 8,
        (2007, x[1], 1964, 1988): 9,
        (2007, x[1], 1964, 1959): 10,
        (2007, x[1], 1964, 1966): 11
    }
    return lookup_table.get(tuple(x), 0)

print(main([2006, 1970, 2013, 1966])) #= 2
print(main([2007, 1979, 1968, 1966])) #= 8
print(main([2007, 1979, 2013, 1988])) #= 5
print(main([2006, 1970, 2013, 1959])) #= 1
print(main([2006, 1969, 2013, 1959])) #= 4